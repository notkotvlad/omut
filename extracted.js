function классПоИсточнику(и) {
  if (!и) return '';
  if (/Казгидромет/i.test(и) && /Гибрид/i.test(и)) return 'hybrid';
  if (/OGIMET/i.test(и) && /Гибрид/i.test(и)) return 'hybrid';
  if (/Гибрид/i.test(и)) return 'hybrid';
  if (/Казгидромет/i.test(и)) return 'kazhydromet';
  if (/OGIMET/i.test(и)) return 'ogimet';
  if (и === 'Open-Meteo') return 'open-meteo';
  if (и === 'met.no') return 'met-no';
  return '';
}
