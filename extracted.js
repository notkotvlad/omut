// Диагностика: пингуем Worker и показываем, какие источники работают.
async function проверитьСоединениеВоркера() {
  const блок = document.getElementById('нас-источники-статус');
  if (!блок) return;
  const инп = document.getElementById('нас-воркер-url');
  const url = ((инп?.value) || WORKER_URL || '').trim().replace(/\/+$/, '');
  блок.style.display = 'block';
  if (!url) {
    блок.style.background = 'rgba(255,180,77,0.10)';
    блок.style.color = '#ffb44d';
    блок.style.border = '1px solid rgba(255,180,77,0.28)';
    блок.innerHTML = '⚠️ URL воркера не указан. Введи адрес, сохрани и попробуй снова.';
    return;
  }
  блок.style.background = 'rgba(77,166,255,0.10)';
  блок.style.color = '#4da6ff';
  блок.style.border = '1px solid rgba(77,166,255,0.25)';
  блок.innerHTML = '⏳ Проверяем ' + url + '...';
  try {
    const контр = new AbortController();
    const тм = setTimeout(() => контр.abort(), 10000);
    const r = await fetch(url + '/api/v1/weather?lat=43.9114&lng=77.1203', { signal: контр.signal });
    clearTimeout(тм);
    if (!r.ok) throw new Error('HTTP ' + r.status);
    const д = await r.json();
    if (!д?.ok) throw new Error('Ответ без ok: ' + JSON.stringify(д).slice(0, 200));
    const s = д.sources || {};
    const строки = [];
    const флажок = v => v && v !== 'none' ? '✅' : '—';
    строки.push('<b>Источники:</b>');
    строки.push(`${флажок(s.meteo)} Текущая погода: <code>${s.meteo || 'none'}</code>`);
    строки.push(`${флажок(s.forecast_daily)} 7-дневный прогноз: <code>${s.forecast_daily || 'none'}</code>`);
    строки.push(`${флажок(s.hydro)} Гидропост: <code>${s.hydro || 'none'}</code>${s.hydro === 'none' ? ' <span style="opacity:.6">(нормально для MVP, см. README)</span>' : ''}`);
    строки.push(`${флажок(s.nowcast)} Наукастинг 1–6ч: <code>${s.nowcast || 'none'}</code>${s.nowcast === 'none' ? ' <span style="opacity:.6">(прототип, см. README)</span>' : ''}`);
    if (д.location?.station) строки.push(`<br><b>Ближайшая станция:</b> ${д.location.station} (${д.location.station_distance_km} км)`);
    if (д.current?.temp_air != null) строки.push(`<b>Сейчас:</b> ${д.current.temp_air}°C, источник: <code>${д.current.source || 'n/a'}</code>`);
    const окК = (s.meteo !== 'none' || s.forecast_daily !== 'none');
    блок.style.background = окК ? 'rgba(0,214,143,0.10)' : 'rgba(255,180,77,0.10)';
    блок.style.color = окК ? '#00d68f' : '#ffb44d';
    блок.style.border = окК ? '1px solid rgba(0,214,143,0.25)' : '1px solid rgba(255,180,77,0.28)';
    блок.innerHTML = (окК ? '✅ Worker отвечает, данные приходят.' : '⚠️ Worker отвечает, но все источники вернули none.') + '<br>' + строки.join('<br>');
  } catch (е) {
    блок.style.background = 'rgba(255,77,77,0.10)';
    блок.style.color = '#ff7a7a';
    блок.style.border = '1px solid rgba(255,77,77,0.28)';
    блок.innerHTML = `❌ Не удалось достучаться: ${е.message}. Проверь URL и что Worker задеплоен.`;
  }
}

