function отрисоватьДавление() {
  const эл = document.getElementById('давл-тело');
  if (!выбМесто || !кеш[выбМесто.id]) {
    показатьСАнимацией(эл, `
      <div style="display:flex;gap:12px;align-items:center">
        <div class="скелет" style="width:80px;height:80px;border-radius:50%"></div>
        <div style="flex:1;display:flex;flex-direction:column;gap:8px">
          <div class="скелет" style="height:30px;width:80%"></div>
          <div class="скелет" style="height:13px;width:55%"></div>
        </div>
      </div>
      <div class="скелет" style="height:48px;border-radius:8px;margin-top:14px"></div>
    `);
    return;
  }
  const п     = кеш[выбМесто.id];
  const ГПА_В_ММ = 0.75006;
  const давлМм = Math.round(п.давление * ГПА_В_ММ);
  const тренд = п.трендДавл;
  const ДАВЛ_СИМВ = { стабильное:'→', падает:'↓', резко_падает:'↓↓', растёт:'↑', резко_растёт:'↑↑' };
  const ДАВЛ_НАЗВ = { стабильное:'стабильное', падает:'падает', резко_падает:'резко падает', растёт:'растёт', резко_растёт:'резко растёт' };
  const давлЦвет = тренд === 'резко_падает' ? 'var(--red)' :
                   тренд === 'падает'        ? 'var(--yellow)' :
                   тренд === 'стабильное'    ? 'var(--green)' : 'var(--blue)';

  // SVG-гауге 720–780 мм рт.ст. (≈ 960–1040 гПа)
  const пцт = Math.max(0.01, Math.min(0.99, (давлМм - 720) / 60));
  const cx=80, cy=72, r=54;
  const θ = (1 - пцт) * Math.PI;
  const xV = (cx + r * Math.cos(θ)).toFixed(1);
  const yV = (cy - r * Math.sin(θ)).toFixed(1);

  const отНормы = давлМм - 760;
  const давлИзмМм = п.давлИзм != null ? Math.round(п.давлИзм * ГПА_В_ММ * 10) / 10 : null;
  const измТекст = давлИзмМм != null
    ? (давлИзмМм >= 0 ? `+${давлИзмМм}` : `${давлИзмМм}`) + ' мм за 24ч'
    : (Math.abs(отНормы) < 2 ? 'норма' : `${отНормы>0?'+':''}${отНормы} от нормы`);

  // 7-дневный линейный график давления
  let график7Дней = '';
  if (п.часВремя && п.часДавл) {
    const сегодняСтр = new Intl.DateTimeFormat('en-CA', { timeZone: 'Asia/Almaty' }).format(new Date());
    const ДНИРУ = ['Вс', 'Пн', 'Вт', 'Ср', 'Чт', 'Пт', 'Сб'];
    const поДням = {};
    п.часВремя.forEach((t, i) => {
      const д = t.slice(0, 10);
      if (!поДням[д]) поДням[д] = [];
      if (п.часДавл[i] != null) поДням[д].push(п.часДавл[i]);
    });
    const дни7 = Object.keys(поДням).sort().filter(д => д >= сегодняСтр).slice(0, 7);
    if (дни7.length >= 2) {
      const средние = дни7.map(д => {
        const arr = поДням[д];
        return arr.length ? arr.reduce((a, b) => a + b, 0) / arr.length : null;
      });
      const валидные = средние.filter(v => v != null);
      if (валидные.length >= 2) {
        const мин = Math.min(...валидные);
        const макс = Math.max(...валидные);
        const диап = макс - мин || 1;
        const нзвДней = дни7.map(д => {
          const дата = new Date(д + 'T12:00:00+05:00');
          return { нзв: ДНИРУ[дата.getDay()], числоМес: `${дата.getDate()}.${String(дата.getMonth()+1).padStart(2,'0')}` };
        });
        const миниМм = мин * ГПА_В_ММ;
        const максиМм = макс * ГПА_В_ММ;
        const диапМм = максиМм - миниМм || 1;
        const бары = средние.map((v, j) => {
          if (v == null) return `<div class="давл-бар-кол"><span class="давл-бар-знач" style="color:var(--text-3)">—</span><div class="давл-бар" style="height:4px;background:var(--border-md)"></div></div>`;
          const valМм = Math.round(v * ГПА_В_ММ);
          const высота = Math.max(6, Math.round(((v * ГПА_В_ММ - миниМм) / диапМм) * 40) + 8);
          const цв = valМм < 754 ? 'var(--red)' : valМм < 760 ? 'var(--yellow)' : valМм < 766 ? 'var(--green)' : 'var(--blue)';
          return `<div class="давл-бар-кол">
            <span class="давл-бар-знач" style="color:${цв}">${valМм}</span>
            <div class="давл-бар" style="height:${высота}px;background:${цв}"></div>
          </div>`;
        }).join('');
        график7Дней = `
          <div class="давл-7дней">
            <div class="давл-бары">${бары}</div>
            <div class="давл-7дней-нзв">${нзвДней.map((н, i) => `<span style="${i===0 ? 'color:var(--green);font-weight:700' : ''}"><span>${н.нзв}</span><span style="color:var(--text-3);font-weight:400">${н.числоМес}</span></span>`).join('')}</div>
            <div class="давл-7дней-метка"><span style="visibility:hidden">·</span><span>прогноз 7 дней, мм рт.ст.</span><span style="visibility:hidden">·</span></div>
          </div>`;
      }
    }
  }

  показатьСАнимацией(эл, `
    <div style="display:flex;align-items:center;gap:16px">
      <svg viewBox="0 0 160 88" width="130" height="70">
        <path d="M ${cx-r} ${cy} A ${r} ${r} 0 0 1 ${cx+r} ${cy}"
              fill="none" stroke="var(--bg-page)" stroke-width="8" stroke-linecap="round"/>
        <path d="M ${cx-r} ${cy} A ${r} ${r} 0 0 1 ${xV} ${yV}"
              fill="none" stroke="${давлЦвет}" stroke-width="8" stroke-linecap="round"/>
        <text x="${cx-r}" y="${cy+14}" fill="var(--text-3)" font-size="9" font-family="Inter,sans-serif" text-anchor="middle">720</text>
        <text x="${cx}"   y="12"       fill="var(--text-3)" font-size="9" font-family="Inter,sans-serif" text-anchor="middle">760</text>
        <text x="${cx+r}" y="${cy+14}" fill="var(--text-3)" font-size="9" font-family="Inter,sans-serif" text-anchor="middle">780</text>
        <text x="${cx}" y="${cy-6}"  fill="var(--text-1)"  font-size="22" font-weight="800" font-family="Inter,sans-serif" text-anchor="middle">${давлМм}</text>
        <text x="${cx}" y="${cy+10}" fill="${давлЦвет}" font-size="11" font-family="Inter,sans-serif" text-anchor="middle">мм</text>
      </svg>
      <div style="flex:1">
        <div class="давл-тренд" style="font-size:16px;font-weight:700;color:${давлЦвет}">${ДАВЛ_СИМВ[тренд]} ${ДАВЛ_НАЗВ[тренд]}</div>
        <div class="давл-изм">${измТекст}</div>
      </div>
    </div>
    ${график7Дней}
  `);
}

