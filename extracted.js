  const ВРЕМЯ_НАЗВ = { утро:'Утро', день:'День', вечер:'Вечер', ночь:'Ночь' };
  const стВ = ['С','СВ','В','ЮВ','Ю','ЮЗ','З','СЗ'][Math.round((п.ветерГрад || 0) / 45) % 8];

  const факторы = [];
  if (дТ.давление >= 15)  факторы.push({ кл:'плюс',  ик:'📊', т:`Давление ${ДАВЛ_НАЗВ[п.трендДавл]} — рыба активна` });
  else if (дТ.давление > 0) факторы.push({ кл:'нейтр', ик:'📊', т:`Давление ${ДАВЛ_НАЗВ[п.трендДавл]}` });
