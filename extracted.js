function getТемпВоды(воздух) {
  const м = new Date().getMonth() + 1;
  const δ = (м >= 12 || м <= 2) ? 3 : (м >= 6 && м <= 8) ? -4 : -1;
  return Math.max(0.5, воздух + δ);
}

function getВремяСуток() {
  const h = new Date().getHours();
  if (h >= 5 && h < 11) return 'утро';
  if (h >= 11 && h < 18) return 'день';
  if (h >= 18 && h < 22) return 'вечер';
  return 'ночь';
}

