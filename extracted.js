function форматДатуКеша(ts) {
  if (!ts) return 'прошлый сеанс';
  const д = new Date(ts);
  const сегодня = new Date();
  const вчера = new Date(Date.now() - 86400000);
  const времяСтр = д.toLocaleTimeString('ru-RU', { hour: '2-digit', minute: '2-digit' });
  if (д.toDateString() === сегодня.toDateString()) return `сегодня в ${времяСтр}`;
  if (д.toDateString() === вчера.toDateString()) return `вчера в ${времяСтр}`;
  return д.toLocaleDateString('ru-RU', { day: 'numeric', month: 'short' }) + ` в ${времяСтр}`;
}

