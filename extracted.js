// ============ ОТРИСОВКА ЛУЧШЕГО ДНЯ ============
function отрисоватьЛучшийДень() {
  const карточка = document.getElementById('лучший-день-карточка');
  const эл = document.getElementById('лучший-день-тело');
  if (!выбМесто || !кеш[выбМесто.id]) { карточка.style.display = 'none'; return; }
  const рыбыД = загрузитьРыбДанные();
  if (!рыбыД[выбРыба] || !выбМесто.fish?.includes(выбРыба)) { карточка.style.display = 'none'; return; }
  карточка.style.display = 'block';
  const п = кеш[выбМесто.id];
  const daily = п.daily;
  if (!daily) return;
  const сегодняСтр = new Intl.DateTimeFormat('en-CA', { timeZone: 'Asia/Almaty' }).format(new Date());
  const старт = Math.max(0, daily.time.findIndex(d => d >= сегодняСтр));
  const дни = daily.time.slice(старт, старт + 7);
  const поДням = {};
  п.часВремя.forEach((t, i) => {
    const д = t.slice(0, 10);
    if (!поДням[д]) поДням[д] = [];
    if (п.часДавл[i] != null) поДням[д].push(п.часДавл[i]);
  });
  const ДНИРУ = ['Вс', 'Пн', 'Вт', 'Ср', 'Чт', 'Пт', 'Сб'];
  const прогнозы = дни.map((датаСтр, i) => {
    const avgДавл = поДням[датаСтр]?.length ? поДням[датаСтр].reduce((a, b) => a + b, 0) / поДням[датаСтр].length : null;
    const предыдДавл = i > 0 && поДням[дни[i - 1]]?.length ? поДням[дни[i - 1]].reduce((a, b) => a + b, 0) / поДням[дни[i - 1]].length : avgДавл;
    let трендДавл = 'стабильное';
    if (avgДавл != null && предыдДавл != null) {
      const δ = avgДавл - предыдДавл;
      if (Math.abs(δ) < 2) трендДавл = 'стабильное';
      else if (δ < -5) трендДавл = 'резко_падает';
      else if (δ < -2) трендДавл = 'падает';
      else if (δ > 5) трендДавл = 'резко_растёт';
      else трендДавл = 'растёт';
    }
    const ветерМс = daily.windspeed_10m_max[старт + i] || 0;
    const шанс = рассчитатьКлёвДень(выбРыба, { трендДавл, темпМакс: daily.temperature_2m_max[старт + i], ветер: ветерМс, осадки: daily.precipitation_sum[старт + i] || 0, датаСтр }, выбМесто.waterType);
    const ветерНапр = (daily.winddirection_10m_dominant || [])[старт + i];
    const ВЕТ_СТРЕЛКИ = ['↑','↗','→','↘','↓','↙','←','↖'];
    const ветерСтр = Math.round(ветерМс) + (ветерНапр != null ? ВЕТ_СТРЕЛКИ[Math.round(ветерНапр / 45) % 8] : '');
    return { датаСтр, шанс, код: (daily.weather_code || daily.weathercode || [])[старт + i], темп: daily.temperature_2m_max[старт + i], трендДавл, ветерСтр, давлСимв: { стабильное: '→', падает: '↓', резко_падает: '↓↓', растёт: '↑', резко_растёт: '↑↑' }[трендДавл] };
  });
  const максШанс = Math.max(...прогнозы.map(д => д.шанс));
  const МЕС = ['янв','фев','мар','апр','май','июн','июл','авг','сен','окт','ноя','дек'];
  const строки = прогнозы.map((д, i) => {
    const лучший = д.шанс === максШанс;
    const дата = new Date(д.датаСтр + 'T12:00:00+05:00');
    const нзв = ДНИРУ[дата.getDay()];
    const деньЧисло = дата.getDate();
    const мес = МЕС[дата.getMonth()];
    const датаТекст = `${нзв} ${деньЧисло} ${мес}`;
    const цвет = цветПцт(д.шанс);
    return `<div class="лд-стр ${лучший ? 'лучший' : ''}"><span class="лд-дата">${датаТекст}${лучший ? ' ⭐' : ''}</span><span class="лд-погода">${погодаЭмодзи(д.код)}</span><span class="лд-темп">+${Math.round(д.темп)}°</span><span class="лд-давл">${д.давлСимв}</span><span class="лд-ветер">${д.ветерСтр}</span><span class="лд-пцт" style="color:${цвет}">${д.шанс}%</span><div class="лд-бар-трек"><div class="лд-бар-зап" style="width:${д.шанс}%;background:${цвет}"></div></div></div>`;
  }).join('');
  показатьСАнимацией(эл, строки);
}

