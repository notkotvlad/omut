// Заполняем <select id="мф-станция"> из кеша справочника (справочникСтанций)
// и выставляем текущее значение из spot.weatherStationId / spot.weatherRegionId.
function заполнитьСтанцииВВыпадашку(spot) {
  const sel = document.getElementById('мф-станция');
  if (!sel) return;
  const текущий = spot?.weatherStationId != null
    ? `${spot.weatherRegionId || 6}:${spot.weatherStationId}`
    : 'auto';
  const станции = (справочникСтанций?.stations || []).filter(s => s.station_id != null);
  // Сортируем станции внутри региона по имени — чтобы в дропдауне было удобно искать.
  const группы = {};
  станции.forEach(s => {
    const rid = s.region_id || 0;
    if (!группы[rid]) группы[rid] = [];
    группы[rid].push(s);
  });
  Object.values(группы).forEach(arr => arr.sort((a, b) => String(a.name).localeCompare(String(b.name), 'ru')));
  const regionName = {
    6: 'Алматинская обл. (region_id=6)',
    22: 'Жетісу (region_id=22)',
  };
  const блоки = ['<option value="auto">Авто — ближайшая по координатам</option>'];
  Object.keys(группы).sort().forEach(rid => {
    блоки.push(`<optgroup label="${regionName[rid] || 'Регион ' + rid}">`);
    группы[rid].forEach(s => {
      const ver = s.verified ? '' : ' *';
      const val = `${rid}:${s.station_id}`;
      блоки.push(`<option value="${val}">${s.name}${ver}</option>`);
    });
    блоки.push('</optgroup>');
  });
  sel.innerHTML = блоки.join('');
  sel.value = текущий;
  if (sel.value !== текущий) sel.value = 'auto'; // если значение отсутствует в списке — откат на авто

  // Обновляем подпись под селектом — показываем счётчик станций.
  const подпись = document.getElementById('мф-станция-счёт');
  if (подпись) {
    const cnt = станции.length;
    const суффикс = cnt ? ` — всего ${cnt} станций в справочнике.` : ' — справочник пуст, нажми ⟳ или проверь URL воркера в настройках.';
    подпись.innerHTML = `Данные по температуре, ветру и давлению будут браться с выбранной станции. Звёздочкой помечены неверифицированные — их <code>station_id</code> нужно сначала подтвердить через «Проверить справочник станций» в настройках.${суффикс}`;
  }
}

