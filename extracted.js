// ============ ОБНОВЛЕНИЕ МАРКЕРОВ ============
function обновитьМаркеры() {
  const места = загрузитьМеста();
  const рыбыД = загрузитьРыбДанные();
  const подсказка = document.getElementById('подсказка-пусто');
  if (подсказка) подсказка.style.display = места.length === 0 ? 'block' : 'none';
  Object.keys(маркеры).forEach(id => {
    if (!места.find(s => s.id === id)) {
      карта.removeLayer(маркеры[id]);
      delete маркеры[id];
    }
  });
  места.forEach(spot => {
    const п = кеш[spot.id];
    const загр = !п && !ошибкиПогоды[spot.id];
    const pct = (п && spot.fish?.includes(выбРыба)) ? рассчитатьКлёв(выбРыба, п, spot.waterType) : null;
    const выбр = выбМесто?.id === spot.id;
    const строкиРыб = (spot.fish || []).map(идР => {
      if (!п) return '';
      const pct2 = рассчитатьКлёв(идР, п, spot.waterType);
      const ц = цветПцт(pct2);
      const рыбаИнф = рыбыД[идР];
      const имя = рыбаИнф ? (рыбаИнф.иконка + ' ' + рыбаИнф.название) : идР;
      return `<div class="поп-рыба-стр"><span class="поп-рыба-им">${имя}</span><div class="поп-рыба-бар"><div class="поп-рыба-зап" style="width:${pct2}%;background:${ц}"></div></div><span class="поп-рыба-пцт" style="color:${ц}">${pct2}%</span></div>`;
    }).join('');
    const попапHTML = `<div class="поп-загол">${spot.name}</div>${строкиРыб || '<div style="color:var(--text-3);font-size:12px">Нет данных погоды</div>'}`;
    if (маркеры[spot.id]) {
      маркеры[spot.id].setIcon(создатьИконку(spot, pct, загр, выбр));
      if (п) маркеры[spot.id].setPopupContent(попапHTML);
    } else {
      const м = L.marker([spot.lat, spot.lng], { icon: создатьИконку(spot, null, true, false) }).addTo(карта);
      м.bindPopup(попапHTML, { maxWidth: 240 });
      м.on('click', () => выбратьМесто(spot));
      маркеры[spot.id] = м;
    }
  });
}

