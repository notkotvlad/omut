function calcTempScore(р, т) {
  if (т < р.темп_мин) return -35;
  if (т < р.темп_опт_мин) return -5;
  if (т <= р.темп_опт_макс) return 25;
  if (т <= р.темп_макс) return 5;
  return -15;
}

function calcWindScore(р, w) {
  if (w < 2) return р.ветер_слабый;
  if (w <= 5) return р.ветер_средний;
  if (w <= 9) return р.ветер_сильный;
  return р.ветер_шторм;
}

function рассчитатьКлёв(идРыбы, п, типВодоёма) {
  const рыбыД = загрузитьРыбДанные();
  const типыД = загрузитьТипыВодоёмов();
  const р = рыбыД[идРыбы];
  if (!р || !п) return 0;
  let s = 50;
  s += р.давление[п.трендДавл] || 0;
  s += calcTempScore(р, п.темпВоды);
  s += р.время[п.времяСуток] || 0;
  s += calcWindScore(р, п.ветерМс);
  s += р.прозрачность[п.прозрКлюч] || 0;
  s += р.луна[п.лунаКлюч] || 0;
  if (типВодоёма && типыД[типВодоёма]) {
    const тип = типыД[типВодоёма];
    s += тип.поправка_давление || 0;
    s += тип.поправка_ветер || 0;
    s += тип.поправка_прозрачность || 0;
    s += (тип.бонус_рыбы && тип.бонус_рыбы[идРыбы]) || 0;
    s += (тип.штраф_рыбы && тип.штраф_рыбы[идРыбы]) || 0;
  }
  return Math.max(0, Math.min(100, Math.round(s)));
}

