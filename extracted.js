function сохранитьМесто() {
  const название = document.getElementById('мф-название').value.trim();
  if (!название) { document.getElementById('мф-название').focus(); return; }
  const рыбы = Array.from(document.querySelectorAll('#мф-рыбы input:checked')).map(el => el.value);
  const тип = document.getElementById('мф-тип').value;
  const заметки = document.getElementById('мф-заметки').value.trim();
  const коордТекст = document.getElementById('мф-коорд').textContent;
  const [lat, lng] = коордТекст.split(',').map(Number);

  // Метеостанция: 'auto' или '6:149' (regionId:stationId)
  const станцияЗнач = document.getElementById('мф-станция')?.value || 'auto';
  let weatherStationId = null;
  let weatherRegionId = null;
  if (станцияЗнач !== 'auto' && станцияЗнач.includes(':')) {
    const [rid, sid] = станцияЗнач.split(':').map(Number);
    if (Number.isFinite(rid) && Number.isFinite(sid)) {
      weatherRegionId = rid;
      weatherStationId = sid;
    }
  }

  const места = загрузитьМеста();
  if (редактируемоеМесто) {
    const i = места.findIndex(s => s.id === редактируемоеМесто.id);
    if (i >= 0) {
      места[i] = { ...места[i], name: название, waterType: тип, fish: рыбы, notes: заметки, weatherStationId, weatherRegionId };
      if (выбМесто?.id === места[i].id) выбМесто = места[i];
    }
  } else {
    const новое = { id: 'spot_' + Date.now(), name: название, lat, lng, waterType: тип, fish: рыбы, notes: заметки, weatherStationId, weatherRegionId, createdAt: Date.now() };
    места.push(новое);
    сохранитьМеста(места);
    закрытьМодалМеста();
    обновитьМаркеры();
    // Сразу загружаем погоду для нового места (через гибрид — чтобы учесть привязку станции)
    запроситьПогодуСРезервом(новое).then(({ данные, источник, гибрид }) => {
      кеш[новое.id] = данные;
      кешИсточник[новое.id] = источник;
      if (гибрид?.hydro) кешГидро[новое.id] = { ...гибрид.hydro, location: гибрид.location };
      обновитьМаркеры();
      if (выбМесто?.id === новое.id) { отрисоватьПрогноз(); отрисоватьДавление(); отрисоватьВетер(); отрисоватьГидрологию(); отрисоватьЛучшийДень(); }
    }).catch(e => console.error('Погода для нового места:', e));
    return;
  }
  сохранитьМеста(места);
  закрытьМодалМеста();
  обновитьМаркеры();
  // После изменения привязки станции — перезагружаем погоду для этой точки
  if (редактируемоеМесто) {
    кешСвежий.delete(редактируемоеМесто.id);
    const spotSaved = места.find(s => s.id === редактируемоеМесто.id);
    if (spotSaved) {
      запроситьПогодуСРезервом(spotSaved).then(({ данные, источник, гибрид }) => {
        кеш[spotSaved.id] = данные;
        кешИсточник[spotSaved.id] = источник;
        кешДаты[spotSaved.id] = Date.now();
        if (гибрид?.hydro) кешГидро[spotSaved.id] = { ...гибрид.hydro, location: гибрид.location }; else delete кешГидро[spotSaved.id];
        кешСвежий.add(spotSaved.id);
        сохранитьКешПогодыLS();
        обновитьМаркеры();
        if (выбМесто?.id === spotSaved.id) { отрисоватьКарточкуМеста(); отрисоватьПрогноз(); отрисоватьДавление(); отрисоватьВетер(); отрисоватьГидрологию(); отрисоватьЛучшийДень(); }
      }).catch(e => console.error('Погода после смены станции:', e));
    }
  }
  if (выбМесто) { отрисоватьКарточкуМеста(); отрисоватьПрогноз(); }
}

function удалитьМесто() {
  if (!редактируемоеМесто) return;
  if (!confirm(`Удалить место «${редактируемоеМесто.name}»?`)) return;
  let места = загрузитьМеста();
  delete кеш[редактируемоеМесто.id];
  if (маркеры[редактируемоеМесто.id]) {
    карта.removeLayer(маркеры[редактируемоеМесто.id]);
    delete маркеры[редактируемоеМесто.id];
  }
  места = места.filter(s => s.id !== редактируемоеМесто.id);
  сохранитьМеста(места);
  if (выбМесто?.id === редактируемоеМесто.id) {
    выбМесто = null;
    отрисоватьКарточкуМеста();
    отрисоватьПрогноз();
    отрисоватьДавление();
    отрисоватьЛучшийДень();
    отрисоватьВетер();
  }
  закрытьМодалМеста();
  обновитьМаркеры();
}

