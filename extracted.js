// Справочник метеостанций (регион + station_id + координаты), подтягивается с воркера
// /api/v1/stations. Используется в модалке места для привязки точки к конкретной станции.
let справочникСтанций = { stations: [], fetched_at: null };

// Ключ кеша бампится при каждом расширении справочника (v3 = 46 станций, apr 2026).
// Старые ключи чистим, чтобы у пользователя не оставался stale список на 3-5 станций.
const STATIONS_CACHE_KEY = 'kaz_stations_cache_v3';
const STATIONS_CACHE_KEYS_TO_PURGE = ['kaz_stations_cache', 'kaz_stations_cache_v1', 'kaz_stations_cache_v2'];

// Синхронное чтение справочника из localStorage — чтобы дропдаун заполнялся сразу,
// без ожидания сети. Асинхронное обновление выполняется в инициализировать().
function загрузитьСправочникСтанцийИзLS() {
  try {
    // Чистим устаревшие ключи — если пользователь пришёл со старой версией.
    for (const k of STATIONS_CACHE_KEYS_TO_PURGE) {
      try { localStorage.removeItem(k); } catch (_) {}
    }
    const raw = localStorage.getItem(STATIONS_CACHE_KEY);
    if (!raw) return;
    const obj = JSON.parse(raw);
    if (obj && Array.isArray(obj.stations)) {
      справочникСтанций = obj;
    }
  } catch (_) { /* ignore */ }
}

// Асинхронное обновление справочника станций с воркера. Сохраняет в localStorage
// для ускоренной загрузки в следующий раз.
async function загрузитьСправочникСтанций() {
  if (!WORKER_URL) return;
  const base = WORKER_URL.replace(/\/+$/, '');
  const контр = (typeof AbortSignal !== 'undefined' && AbortSignal.timeout)
    ? AbortSignal.timeout(ТАЙМАУТ_ГИБРИД_МС)
    : undefined;
  // Cache-bust: форсим свежий ответ, чтобы не застрять на edge cache воркера.
  const r = await fetch(`${base}/api/v1/stations?t=${Date.now()}`, контр ? { signal: контр } : {});
  if (!r.ok) throw new Error(`Справочник HTTP ${r.status}`);
  const данные = await r.json();
  if (!данные || !Array.isArray(данные.stations)) throw new Error('Справочник: неверный формат ответа');
  справочникСтанций = {
    stations: данные.stations,
    regions: данные.regions || {},
    fetched_at: new Date().toISOString(),
  };
  try {
    localStorage.setItem(STATIONS_CACHE_KEY, JSON.stringify(справочникСтанций));
  } catch (_) {}
  // Если модалка места сейчас открыта — перерисуем селект, чтобы показать свежие станции.
  const модал = document.getElementById('модал-место-оверлей');
  if (модал && модал.style.display !== 'none') {
    try { заполнитьСтанцииВВыпадашку(редактируемоеМесто); } catch (_) {}
  }
}

