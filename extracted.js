function getTrend(часДавл, часВремя, текВремя) {
  const i = часВремя.indexOf(текВремя);
  if (i < 3) return 'стабильное';
  const δ = часДавл[i] - часДавл[i - 3];
  if (Math.abs(δ) < 2) return 'стабильное';
  if (δ < -5) return 'резко_падает';
  if (δ < -2) return 'падает';
  if (δ > 5)  return 'резко_растёт';
  return 'растёт';
}

function getПрозр(часОс, часВремя, текВремя) {
  const i = часВремя.indexOf(текВремя);
  if (i < 0) return { ключ:'чистая', назв:'чистая', симв:'💧' };
  let с = 0;
  for (let j = Math.max(0, i - 47); j <= i; j++) с += (часОс[j] || 0);
  if (с < 1) return { ключ:'чистая',        назв:'чистая',        симв:'💧' };
  if (с < 5) return { ключ:'слегка_мутная', назв:'слегка мутная', симв:'🌊' };
  return          { ключ:'мутная',          назв:'мутная',         симв:'🌫️' };
}

function getFazaLuny() {
  const t0 = new Date('2000-01-06T18:14:00Z');
  const цикл = 29.53058867;
  const tNow = new Date();
  const д = ((tNow - t0) / 86400000 % цикл + цикл) % цикл;
  if (д < 1.85 || д > 27.68) return { ключ: 'новолуние', назв: 'Новолуние', симв: '🌑' };
  if (д < 13.77) return { ключ: 'растущая', назв: 'Растущая луна', симв: '🌒' };
  if (д < 16.38) return { ключ: 'полнолуние', назв: 'Полнолуние', симв: '🌕' };
  return { ключ: 'убывающая', назв: 'Убывающая луна', симв: '🌘' };
}

