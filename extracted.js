// ============ ЗАПРОС К ГИБРИДНОМУ ВОРКЕРУ (Казгидромет + ecodata + OGIMET) ============
// Возвращает объект контракта /api/v1/weather (см. omut-worker/README.md).
// При любой ошибке — кидает исключение, caller должен сделать fallback на Open-Meteo.
async function запроситьПогодуГибрид(spot, signal) {
  if (!WORKER_URL) throw new Error('Гибрид: WORKER_URL не задан');
  if (ПРЕДПОЧТЕНИЕ_ИСТОЧНИКА === 'open-meteo') throw new Error('Гибрид: отключён пользователем');
  const base = WORKER_URL.replace(/\/+$/, '');
  let url = `${base}/api/v1/weather?lat=${spot.lat}&lng=${spot.lng}`;
  // Если у точки явно привязана станция — передаём её во воркер, там haversine пропускается
  if (spot && Number.isFinite(spot.weatherStationId)) {
    url += `&station_id=${encodeURIComponent(spot.weatherStationId)}`;
  }
  if (spot && Number.isFinite(spot.weatherRegionId)) {
    url += `&region_id=${encodeURIComponent(spot.weatherRegionId)}`;
  }
  const r = await fetch(url, signal ? { signal } : {});
  if (!r.ok) throw new Error(`Гибрид HTTP ${r.status}`);
  const данные = await r.json();
  if (!данные || данные.ok === false) throw new Error('Гибрид: ответ без ok');
  return данные;
}

// Склейка: поверх базовых данных Open-Meteo/MET Norway накладываем поля из гибрида,
// где они есть и считаются более точными. Источники — см. таблицу в брифинге.
function склейкаГибрид(базовые, гибрид) {
  if (!базовые) return базовые;
  if (!гибрид) return базовые;

  const итог = { ...базовые };

  // Текущая погода: temp_air, wind_ms, wind_deg, pressure из kazhydromet/ogimet
  const cur = гибрид.current;
  if (cur && Number.isFinite(cur.temp_air))       итог.темпВоздух = cur.temp_air;
  if (cur && Number.isFinite(cur.wind_ms))        итог.ветерМс    = cur.wind_ms;
  if (cur && Number.isFinite(cur.wind_deg))       итог.ветерГрад  = cur.wind_deg;
  if (cur && Number.isFinite(cur.pressure_hpa))   итог.давление   = cur.pressure_hpa;

  // Температура воды — приоритет: реальный гидропост, иначе не трогаем
  if (гидропостКорректен(гибрид) && Number.isFinite(гибрид.hydro.water_temp)) {
    итог.темпВоды = гибрид.hydro.water_temp;
  }

  // Сохраняем сырые данные для UI (карточка гидрологии, бейджи источников)
  итог._гибрид = гибрид;
  итог._источникТекущего =
    (cur?.source === 'ogimet' && 'OGIMET') ||
    (cur?.source === 'kazhydromet' && 'Казгидромет') ||
    null;
  return итог;
}

function гидропостКорректен(гибрид) {
  const h = гибрид?.hydro;
  return h && (h.water_temp != null || h.water_level_cm != null || h.flow_m3s != null);
}

// ============ ЗАПРОС ПОГОДЫ ============
async function запроситьПогоду(spot, signal) {
  const url = [
    'https://api.open-meteo.com/v1/forecast',
    `?latitude=${spot.lat}&longitude=${spot.lng}`,
    '&current=temperature_2m,pressure_msl,windspeed_10m,winddirection_10m,precipitation',
    '&hourly=pressure_msl,precipitation,temperature_2m,soil_temperature_6cm',
    '&daily=weather_code,temperature_2m_max,temperature_2m_min',
    ',precipitation_sum,precipitation_probability_max,windspeed_10m_max,winddirection_10m_dominant',
    '&timezone=Asia%2FAlmaty&past_days=2&forecast_days=7',
  ].join('');

  const r = await fetch(url, signal ? { signal } : {});
  if (!r.ok) {
    const текст = await r.text().catch(() => '');
    throw new Error(`HTTP ${r.status}${текст ? ': ' + текст.slice(0, 120) : ''}`);
  }
  const д = await r.json();

  const темпВоздух  = д.current.temperature_2m;
  // current.time приходит с минутами ("14:15"), hourly — по часам ("14:00")
  // обрезаем до часа для корректного indexOf
  const текВремяЧас = д.current.time.slice(0, 13) + ':00';
  const трендДавл   = getTrend(д.hourly.pressure_msl, д.hourly.time, текВремяЧас);
  const прозр       = getПрозр(д.hourly.precipitation, д.hourly.time, текВремяЧас);
  const луна        = getFazaLuny();

  const iТек = д.hourly.time.indexOf(текВремяЧас);
  const давлТек = iТек >= 0 ? д.hourly.pressure_msl[iТек] : д.current.pressure_msl;
  const давл24н = iТек >= 24 ? д.hourly.pressure_msl[iТек - 24] : null;
  const давлИзм = давл24н != null ? Math.round((давлТек - давл24н) * 10) / 10 : null;

  // поддержка нового (weather_code) и старого (weathercode) имени поля
  const daily = д.daily;
  if (daily && !daily.weather_code && daily.weathercode) daily.weather_code = daily.weathercode;

  const почвТемп = iТек >= 0 ? д.hourly.soil_temperature_6cm?.[iТек] : null;

  return {
    темпВоздух,
    давление:    д.current.pressure_msl,
    ветерМс:     д.current.windspeed_10m,
    ветерГрад:   д.current.winddirection_10m,
    темпВоды:    почвТемп != null ? Math.max(0.5, почвТемп) : getТемпВоды(темпВоздух),
    трендДавл,
    давлИзм,
    прозрКлюч:   прозр.ключ,
    прозрНазв:   прозр.назв,
    прозрСимв:   прозр.симв,
    лунаКлюч:    луна.ключ,
    лунаНазв:    луна.назв,
    лунаСимв:    луна.симв,
    времяСуток:  getВремяСуток(),
    daily,
    часДавл:     д.hourly.pressure_msl,
    часВремя:    д.hourly.time,
    часОсадки:   д.hourly.precipitation,
    текВремя:    текВремяЧас,
  };
}

// ============ РЕЗЕРВНЫЙ API (met.no) ============
async function запроситьПогодуМетНО(spot, signal) {
  const url = `https://api.met.no/weatherapi/locationforecast/2.0/compact?lat=${spot.lat.toFixed(4)}&lon=${spot.lng.toFixed(4)}`;
  const r = await fetch(url, { signal });
  if (!r.ok) throw new Error(`met.no HTTP ${r.status}`);
  const д = await r.json();
  const серия = д.properties?.timeseries;
  if (!серия?.length) throw new Error('met.no: нет данных');

  // Текущий час в Алмате: "2026-04-07T13:00"
  const текВремяЧас = new Date()
    .toLocaleString('sv-SE', { timeZone: 'Asia/Almaty' })
    .slice(0, 13).replace(' ', 'T') + ':00';

  // UTC → час Алматы
  const toЧас = utcStr => new Date(utcStr)
    .toLocaleString('sv-SE', { timeZone: 'Asia/Almaty' })
    .slice(0, 13).replace(' ', 'T') + ':00';

  // Почасовые массивы
  const часВремя = [], часДавл = [], часОсадки = [];
  серия.forEach(е => {
    часВремя.push(toЧас(е.time));
    часДавл.push(е.data?.instant?.details?.air_pressure_at_sea_level ?? null);
    часОсадки.push(е.data?.next_1_hours?.details?.precipitation_amount ?? 0);
  });

  // met.no начинается с текущего часа+1 — ищем ближайший доступный
  let iТек = часВремя.indexOf(текВремяЧас);
  if (iТек < 0) iТек = часВремя.findIndex(t => t > текВремяЧас);
  if (iТек < 0) iТек = 0;
  const эфВремя = часВремя[iТек];  // эффективное время для getTrend/getПрозр

  const дет = серия[iТек]?.data?.instant?.details ?? {};
  const темпВоздух = дет.air_temperature ?? 0;
  const давление   = дет.air_pressure_at_sea_level ?? 1013;
  const ветерМс    = дет.wind_speed ?? 0;
  const ветерГрад  = дет.wind_from_direction ?? 0;

  // Тренд по ближайшим данным (у met.no нет истории — смотрим вперёд)
  let трендДавл;
  if (iТек >= 3) {
    трендДавл = getTrend(часДавл, часВремя, эфВремя);
  } else {
    const n = Math.min(3, часДавл.length - 1);
    const δ = n > 0 ? (часДавл[n] ?? 0) - (часДавл[0] ?? 0) : 0;
    if (Math.abs(δ) < 2)       трендДавл = 'стабильное';
    else if (δ < -5)            трендДавл = 'резко_падает';
    else if (δ < -2)            трендДавл = 'падает';
    else if (δ > 5)             трендДавл = 'резко_растёт';
    else                        трендДавл = 'растёт';
  }
  const прозр = getПрозр(часОсадки, часВремя, эфВремя);
  const луна  = getFazaLuny();

  const давл24н = iТек >= 24 ? часДавл[iТек - 24] : null;
  const давлИзм = давл24н != null ? Math.round((давление - давл24н) * 10) / 10 : null;

  // Дневные агрегаты
  const поДням = {};
  серия.forEach((е, i) => {
    const дата = часВремя[i]?.slice(0, 10);
    if (!дата) return;
    if (!поДням[дата]) поДням[дата] = { temps: [], winds: [], windirs: [], ос: [], код: null };
    const t = е.data?.instant?.details?.air_temperature;
    const w = е.data?.instant?.details?.wind_speed;
    const wd = е.data?.instant?.details?.wind_from_direction;
    const o = е.data?.next_1_hours?.details?.precipitation_amount;
    if (t != null) поДням[дата].temps.push(t);
    if (w != null) поДням[дата].winds.push(w);
    if (wd != null) поДням[дата].windirs.push(wd);
    if (o != null) поДням[дата].ос.push(o);
    if (!поДням[дата].код)
      поДням[дата].код = е.data?.next_6_hours?.summary?.symbol_code
        || е.data?.next_1_hours?.summary?.symbol_code || null;
  });
  const симвВВМО = к => {
    if (!к) return 0;
    if (/thunder/.test(к)) return 95;
    if (/snow|sleet/.test(к)) return 71;
    if (/rain|shower|drizzle/.test(к)) return 61;
    if (/fog/.test(к)) return 45;
    if (/cloudy/.test(к)) return 3;
    if (/partlycloudy|fair/.test(к)) return 2;
    return 0;
  };
  const дни = Object.keys(поДням).sort();
  const daily = {
    time: дни,
    temperature_2m_max: дни.map(д => поДням[д].temps.length ? Math.max(...поДням[д].temps) : 0),
    temperature_2m_min: дни.map(д => поДням[д].temps.length ? Math.min(...поДням[д].temps) : 0),
    weather_code: дни.map(д => симвВВМО(поДням[д].код)),
    precipitation_sum: дни.map(д => поДням[д].ос.reduce((a, b) => a + b, 0)),
    precipitation_probability_max: дни.map(д => {
      // met.no не отдаёт вероятность осадков — оцениваем по суточной сумме осадков
      const суммаОс = поДням[д].ос.reduce((a, b) => a + b, 0);
      if (суммаОс > 10) return 85;
      if (суммаОс > 5)  return 65;
      if (суммаОс > 2)  return 45;
      if (суммаОс > 0.5) return 25;
      return 5;
    }),
    windspeed_10m_max: дни.map(д => поДням[д].winds.length ? Math.max(...поДням[д].winds) : 0),
    winddirection_10m_dominant: дни.map(д => {
      const dirs = поДням[д].windirs;
      if (!dirs.length) return 0;
      const sinSum = dirs.reduce((a, d) => a + Math.sin(d * Math.PI / 180), 0);
      const cosSum = dirs.reduce((a, d) => a + Math.cos(d * Math.PI / 180), 0);
      return Math.round((Math.atan2(sinSum, cosSum) * 180 / Math.PI + 360) % 360);
    }),
  };

  return {
    темпВоздух, давление, ветерМс, ветерГрад,
    темпВоды: getТемпВоды(темпВоздух),
    трендДавл, давлИзм,
    прозрКлюч: прозр.ключ, прозрНазв: прозр.назв, прозрСимв: прозр.симв,
    лунаКлюч: луна.ключ, лунаНазв: луна.назв, лунаСимв: луна.симв,
    времяСуток: getВремяСуток(),
    daily, часДавл, часВремя, часОсадки, текВремя: эфВремя,
  };
}

// ============ ОБЁРТКА С РЕЗЕРВНЫМ API ============
// Гибридная стратегия:
//  1) Получаем базовые данные (Open-Meteo → met.no) — это ГАРАНТИРОВАННЫЙ путь.
//     Без них расчёт клёва невозможен (нужны hourly pressure, daily forecast и т.п.).
//  2) Параллельно пытаемся забрать гибрид с прокси-воркера (Казгидромет + OGIMET + hydroposts)
//     с коротким таймаутом (≤5 с). Если получилось — склеиваем поверх базы.
//  3) Если гибрид не ответил — молча продолжаем с Open-Meteo (UX не ломается).
//
// Таким образом сервис никогда не "виснет" из-за Казгидромета и остаётся корректным
// даже если WORKER_URL не задан.
async function запроситьПогодуСРезервом(spot) {
  // 1. Запускаем гибрид параллельно (best-effort)
  const гибридПромис = (async () => {
    if (!WORKER_URL || ПРЕДПОЧТЕНИЕ_ИСТОЧНИКА === 'open-meteo') return null;
    const контр = new AbortController();
    const тм = setTimeout(() => контр.abort(), ТАЙМАУТ_ГИБРИД_МС);
    try {
      const данные = await запроситьПогодуГибрид(spot, контр.signal);
      clearTimeout(тм);
      return данные;
    } catch (е) {
      clearTimeout(тм);
      console.warn(`Гибрид (${spot.name}): ${е.message}`);
      return null;
    }
  })();

  // 2. Базовая погода — Open-Meteo (основной)
  let базовые = null;
  let источникБаза = null;
  const контр1 = new AbortController();
  const тм1 = setTimeout(() => контр1.abort(), 12000);
  try {
    базовые = await запроситьПогоду(spot, контр1.signal);
    источникБаза = 'Open-Meteo';
  } catch (е1) {
    console.warn(`Open-Meteo (${spot.name}): ${е1.message} — пробуем met.no`);
  } finally {
    clearTimeout(тм1);
  }

  // 3. Резерв — met.no
  if (!базовые) {
    const контр2 = new AbortController();
    const тм2 = setTimeout(() => контр2.abort(), 12000);
    try {
      базовые = await запроситьПогодуМетНО(spot, контр2.signal);
      источникБаза = 'met.no';
    } catch (е2) {
      clearTimeout(тм2);
      throw new Error(`Все API недоступны (${е2.message})`);
    }
    clearTimeout(тм2);
  }

  // 4. Ждём гибрид (он уже практически готов или nil)
  const гибрид = await гибридПромис;

  // 5. Склеиваем
  const данные = гибрид ? склейкаГибрид(базовые, гибрид) : базовые;
  let источник = источникБаза;
  if (гибрид) {
    // Если Казгидромет/OGIMET дали current — отмечаем гибрид
    if (данные._источникТекущего) {
      источник = `Гибрид (${данные._источникТекущего} + ${источникБаза})`;
    } else if (гидропостКорректен(гибрид)) {
      источник = `Гибрид (Казгидромет hydro + ${источникБаза})`;
    }
  }
  return { данные, источник, гибрид };
}

// ============ ЗАГРУЗКА ПОГОДЫ ============
async function загрузитьПогоду() {
  обновитьМаркеры();
  const места = загрузитьМеста();
  await Promise.allSettled(места.map(async spot => {
    try {
      delete ошибкиПогоды[spot.id];
      const { данные, источник, гибрид } = await запроситьПогодуСРезервом(spot);
      кеш[spot.id] = данные;
      кешДаты[spot.id] = Date.now();
      кешИсточник[spot.id] = источник;
      if (гибрид?.hydro) кешГидро[spot.id] = { ...гибрид.hydro, location: гибрид.location }; else delete кешГидро[spot.id];
      if (гибрид) кешВоркер[spot.id] = гибрид; else delete кешВоркер[spot.id];
      кешСвежий.add(spot.id);
      сохранитьКешПогодыLS();
      обновитьМаркеры();
      обновитьБаннерАПИ();
      if (выбМесто?.id === spot.id) {
        отрисоватьПрогноз();
        отрисоватьДавление();
        отрисоватьВетер();
        отрисоватьГидрологию();
        отрисоватьЛучшийДень();
      }
    } catch (е) {
      const сообщение = е.message || 'Все источники недоступны';
      ошибкиПогоды[spot.id] = сообщение;
      console.error(`Ошибка погоды «${spot.name}»:`, е);
      обновитьМаркеры();
      обновитьБаннерАПИ();
      if (выбМесто?.id === spot.id) {
        if (кеш[spot.id]) {
          // Есть кеш — показываем его, метка устаревания будет в баннере и прогнозе
          отрисоватьПрогноз();
          отрисоватьДавление();
          отрисоватьВетер();
          отрисоватьГидрологию();
          отрисоватьЛучшийДень();
        } else {
          отрисоватьПрогнозОшибка(сообщение);
        }
      }
    }
  }));
}

function отрисоватьПрогнозОшибка(сообщение) {
  const эл = document.getElementById('прогноз-тело');
  if (эл) эл.innerHTML = `
    <div style="text-align:center;padding:24px 16px;color:var(--text-3)">
      <div style="font-size:28px;margin-bottom:8px">⚠️</div>
      <div style="font-size:13px;color:var(--text-2);margin-bottom:4px">Не удалось загрузить данные</div>
      <div style="font-size:11px;color:var(--text-3);margin-bottom:16px">${сообщение}</div>
      <button class="кн-вторичная" onclick="повторитьПогоду()" style="font-size:12px;padding:6px 16px">🔄 Повторить</button>
    </div>`;
  ['давл-тело','ветер-тело','лучший-день-тело'].forEach(id => {
    const э = document.getElementById(id);
    if (э) э.innerHTML = `<div style="text-align:center;padding:12px;color:var(--text-3);font-size:12px">⚠️ Нет данных</div>`;
  });
  // Гидро-карточка скрывается при полном отсутствии данных
  const гк = document.getElementById('гидро-карточка');
  if (гк) гк.style.display = 'none';
}

function повторитьПогоду() {
  // кеш и кешИсточник НЕ сбрасываем — они нужны как fallback если API снова недоступен
  ошибкиПогоды = {};
  кешСвежий = new Set();
  обновитьБаннерАПИ();
  обновитьМаркеры();
  загрузитьПогоду();
}

function обновитьБаннерАПИ() {
  const баннер = document.getElementById('апи-баннер');
  if (!баннер) return;
  const ошибочные = Object.keys(ошибкиПогоды);
  if (ошибочные.length === 0) { баннер.style.display = 'none'; баннер.innerHTML = ''; return; }

  // Разделяем: у кого есть стале-кеш, у кого нет данных вообще
  const сКешем  = ошибочные.filter(id => кеш[id] && !кешСвежий.has(id));
  const безДанных = ошибочные.filter(id => !кеш[id]);

  let текст;
  if (безДанных.length === 0 && сКешем.length > 0) {
    // Все ошибочные места показывают кешированные данные
    const минДата = Math.min(...сКешем.map(id => кешДаты[id] || 0).filter(Boolean));
    текст = `⏱ Сервис погоды недоступен. Показаны данные от ${форматДатуКеша(минДата || 0)}`;
  } else if (сКешем.length > 0) {
    // Часть мест с кешем, часть — без данных
    const минДата = Math.min(...сКешем.map(id => кешДаты[id] || 0).filter(Boolean));
    текст = `⏱ Частично кешированные данные от ${форматДатуКеша(минДата || 0)}, API недоступен`;
  } else {
    // Нет никакого кеша — первый запуск или данные слишком старые
    const первоеСообщение = Object.values(ошибкиПогоды)[0] || 'Нет ответа';
    текст = `⚠️ Сервис погоды недоступен: ${первоеСообщение}`;
  }

  баннер.style.display = 'flex';
  баннер.innerHTML = `${текст} <button onclick="повторитьПогоду()">🔄 Повторить</button>`;
}

